int		ft_sqrt(int nb)
{
	long long sq;

	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	sq = 0;
	while (sq * sq <= nb)
	{
		if (sq * sq == nb)
			return (sq);
		sq++;
	}
	return (0);
}
