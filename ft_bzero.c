#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	unsigned int	i;

	if (n == 0)
		return (s);
	i = 0;
	while (i < n)
	{
		*((char *)s + i) = 0;
		i++;
	}
	return (s);
}
